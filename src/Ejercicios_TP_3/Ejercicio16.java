public class Ejercicio16 {
    public static void main(String[] args){
        System.out.println("Números del 1 al 30 con WHILE");
        int contador = 1;
        while (contador <= 30) {
            System.out.println("Número: " + contador);
            contador++;
        }
        System.out.println("¡Bucle terminado!");
    }
}
